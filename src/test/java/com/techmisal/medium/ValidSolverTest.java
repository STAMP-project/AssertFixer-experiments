package com.techmisal.medium;

import org.junit.Assert;
import org.junit.Test;

public class ValidSolverTest {

	private ValidSudoku validSudoku = new ValidSudoku();

	@Test
	public void testValidSudoku() {
		char[][] board = new char[][] {{'5','3','.','.','7','.','.','.','.'},
			{'6','.','.','1','9','5','.','.','.'},
			{'.','9','8','.','.','.','.','6','.'},
			{'8','.','.','.','6','.','.','.','3'},
			{'4','.','.','8','.','3','.','.','1'},
			{'7','.','.','.','2','.','.','.','6'},
			{'.','6','.','.','.','.','2','8','.'},
			{'.','.','.','4','1','9','.','.','5'},
			{'.','.','.','.','8','.','.','7','9'}};
		Assert.assertTrue(validSudoku.isValidSudoku(board));
	}
	
	@Test
	public void testInValidSudoku() {
		char[][] board = new char[][] {{'8','3','.','.','7','.','.','.','.'},
			{'6','.','.','1','9','5','.','.','.'},
			{'.','9','8','.','.','.','.','6','.'},
			{'8','.','.','.','6','.','.','.','3'},
			{'4','.','.','8','.','3','.','.','1'},
			{'7','.','.','.','2','.','.','.','6'},
			{'.','6','.','.','.','.','2','8','.'},
			{'.','.','.','4','1','9','.','.','5'},
			{'.','.','.','.','8','.','.','7','9'}};
		Assert.assertFalse(validSudoku.isValidSudoku(board));
	}
}